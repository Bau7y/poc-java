    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int id; String cedula; int telefono; int estado;
        String nombre; String apellidos; String direccion;
        
        if (txtId.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", 
                                       "Aviso Importante", JOptionPane.WARNING_MESSAGE);
  
        } else {
            String regex = "^\\d+$";
            Pattern pattern = Pattern.compile(regex);
            Matcher matcher = pattern.matcher(txtId.getText());
            if (matcher.matches()){
                id = Integer.parseInt(txtId.getText());
                cedula = txtCedula.getText();
                telefono = Integer.parseInt(txtTelefono.getText());
                apellidos = txtApellidos.getText();
                nombre = txtNombre.getText();
                estado = cmbxEstado.getSelectedIndex();
                direccion = txtDireccion.getText();
                

                objCliente clnt = new objCliente(id, cedula, telefono, nombre, apellidos, direccion, estado);
                
                datos.agregarCliente(clnt);

                JOptionPane.showMessageDialog(this, "Informacion guardada!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            
                this.limpiar();
                
            } else { 
                JOptionPane.showMessageDialog(this, "El campo indentificacion solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
                txtId.setText("");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtId.getText());

        if (matcher.matches()){
            if (datos.buscarBorrarClnt(Integer.parseInt(txtId.getText() ))){
                JOptionPane.showMessageDialog(this, "Habitacion borrada!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();
            } else{
                JOptionPane.showMessageDialog(this, "Id no encontrado...", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "El campo indentificacion solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
            txtId.setText("");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

