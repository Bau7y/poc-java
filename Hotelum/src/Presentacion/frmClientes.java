    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtId.getText());
        
        if (matcher.matches()){
            int id = Integer.parseInt(txtId.getText());
            objCliente clnt = datos.getCliente( id );
            
            if (clnt != null){
                JOptionPane.showMessageDialog(this, "Cliente encontrad@!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                txtApellidos.setText(clnt.getApellido());
                txtCedula.setText(clnt.getCedula());
                txtTelefono.setText(String.valueOf(clnt.getTelefono()));
                txtDireccion.setText(clnt.getDireccion());
                txtNombre.setText(clnt.getNombre());
                cmbxEstado.setSelectedIndex(clnt.getEstado());
                
            } else {
                JOptionPane.showMessageDialog(this, "El id digitado no existe", "Aviso", JOptionPane.WARNING_MESSAGE);
                txtId.setText("");
            }
            
        } else{
            JOptionPane.showMessageDialog(this, "El campo indentificacion solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

