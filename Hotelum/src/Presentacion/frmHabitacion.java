    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtIdentificacion.getText());
        
        if (matcher.matches()){
            int id = Integer.parseInt(txtIdentificacion.getText());
            objHabitacion hab = datos.getHabitacion( id );
            
            if (hab != null){
                JOptionPane.showMessageDialog(this, "Habitación encontrada!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                txtObserva.setText(hab.getDetalles());
                cmbxCategoria.setSelectedIndex(hab.getCategoria());
                cmbxClase.setSelectedIndex(hab.getClase());
                cmbxEdificio.setSelectedIndex(hab.getEdificio());
                cmbxEstado.setSelectedIndex(hab.getEstado());
            } else {
                JOptionPane.showMessageDialog(this, "El id digitado no existe", "Aviso", JOptionPane.WARNING_MESSAGE);
                txtIdentificacion.setText("");
            }
            
        } else{
            JOptionPane.showMessageDialog(this, "El campo indentificacion solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

