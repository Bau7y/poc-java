    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtIdentificacion.getText());

        if (matcher.matches()){
            if (datos.buscarBorrar(Integer.parseInt(txtIdentificacion.getText() ))){
                JOptionPane.showMessageDialog(this, "Habitacion borrada!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                txtIdentificacion.setText("");
            } else{
                JOptionPane.showMessageDialog(this, "Id no encontrado...", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "El campo indentificacion solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
            txtIdentificacion.setText("");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtIdentificacion.getText());

        if ( matcher.matches() ){
            int id = Integer.parseInt(txtIdentificacion.getText()); 
            int nuevaCate; int nuevaClase; int nuevEdif; int nuevEstado; int nuevoPiso;
            String nuevDetalle; 
            objHabitacion modHab = datos.getHabitacion(id);
            
            if ( modHab != null ) {
                int respuesta = JOptionPane.showConfirmDialog(this, "Se encontró la habitación, desea modificarla con los valores actuales?", "Aviso", JOptionPane.YES_NO_OPTION);
                if (respuesta == JOptionPane.YES_OPTION){
                    int idx = datos.getIndex(id);
                    nuevaCate = cmbxCategoria.getSelectedIndex();
                    nuevaClase = cmbxClase.getSelectedIndex();
                    nuevEdif = cmbxEdificio.getSelectedIndex();
                    nuevEstado = cmbxEstado.getSelectedIndex();
                    nuevDetalle = txtObserva.getText();
                    Object valor = txtPiso.getValue();
                    Number valSpin = (Number) valor;
                    nuevoPiso = valSpin.intValue();
                    
                    objHabitacion modiHab = new objHabitacion(id, nuevaCate, nuevaClase, nuevEdif, nuevoPiso, nuevEstado, nuevDetalle);
                    datos.modHab(idx, modiHab);
                    
                    txtIdentificacion.setText("");
                    txtObserva.setText("");
                    cmbxCategoria.setSelectedIndex(0);
                    cmbxClase.setSelectedIndex(0);
                    cmbxEdificio.setSelectedIndex(0);
                    cmbxEstado.setSelectedIndex(0);
                    JOptionPane.showMessageDialog(this, "Se modificó la habitación!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "El id digitado no existe", "Aviso", JOptionPane.WARNING_MESSAGE);
                txtIdentificacion.setText("");
            }
        
        } else {
            JOptionPane.showMessageDialog(this, "El campo indentificacion solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
            txtIdentificacion.setText("");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

