    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int id; int categ; int clase; int edificio; int piso; int estado;
        String observacion;
        
        if (txtIdentificacion.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", 
                                       "Aviso Importante", JOptionPane.WARNING_MESSAGE);
  
        } else {
            String regex = "^\\d+$";
            Pattern pattern = Pattern.compile(regex);
            Matcher matcher = pattern.matcher(txtIdentificacion.getText());
            if (matcher.matches()){
                id = Integer.parseInt(txtIdentificacion.getText());
                categ = cmbxCategoria.getSelectedIndex();
                clase = cmbxClase.getSelectedIndex();
                edificio = cmbxEdificio.getSelectedIndex();

                Object valor = txtPiso.getValue();
                Number valSpin = (Number) valor;
                piso = valSpin.intValue();
                estado = cmbxEstado.getSelectedIndex();
                observacion = txtObserva.getText();

                objHabitacion habitacion = new objHabitacion(id, categ, clase, edificio, piso, estado, observacion);
                habitacion.mostrarInfo();
                
                datos.agregarHab(habitacion);

                JOptionPane.showMessageDialog(this, "Informacion guardada!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            
                txtIdentificacion.setText("");
                txtObserva.setText("");
                cmbxCategoria.setSelectedIndex(0);
                cmbxClase.setSelectedIndex(0);
                cmbxEdificio.setSelectedIndex(0);
                cmbxEstado.setSelectedIndex(0);
            } else { 
                JOptionPane.showMessageDialog(this, "El campo indentificacion solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
                txtIdentificacion.setText("");
            }
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

