    private void limpiar(){
        txtCedCli.setText("");
        txtCliente.setText("");
        txtDetalles.setText("");
        txtMonto.setText("");
        txtConsecutivo.setText("");
        cmbxEstado.setSelectedIndex(0);
    }
    
    private void limpiarDetalle(){
        txtCat.setText("");
        txtIdHab.setText("");
        txtClaseHab.setText("");
        LocalDate miFecha = LocalDate.now();
        ZoneId miZona = ZoneId.systemDefault();
        Date actual = Date.from(miFecha.atStartOfDay(miZona).toInstant());
        
        dtpInicio.setDate(actual);
        dtpFin.setDate(actual);
        
    }
    
