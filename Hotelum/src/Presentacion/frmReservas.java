    private void txtCedCliFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCedCliFocusLost
        if ( !txtCedCli.getText().equals("") ){
            String nombreCliente = datos.BuscarCliente(txtCedCli.getText());
            if (!nombreCliente.equals("") ){
                txtCliente.setText(nombreCliente);
            }
        } else {
            txtCliente.setText("No encontrado");
            txtCedCli.requestFocus();
        }
    }//GEN-LAST:event_txtCedCliFocusLost

    private void txtIdHabFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtIdHabFocusLost
        if ( !txtIdHab.getText().equals("")) {
            String nomClase = datos.BuscarClaseHab( Integer.parseInt(txtIdHab.getText()) );
            String nomCat = datos.BuscarCateHab( Integer.parseInt(txtCat.getText()) );
            
            txtClaseHab.setText(nomClase);
            txtCat.setText(nomCat);
        
        } else{
            txtClaseHab.setText("No encontrado");
            txtCat.setText("No encontrado");
        }
    }//GEN-LAST:event_txtIdHabFocusLost

