    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtCedula.getText());

        if (matcher.matches()){
            if (datos.buscarBorrarEmp(Integer.parseInt(txtCedula.getText() ))){
                JOptionPane.showMessageDialog(this, "Empleado borrado!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                txtCedula.setText("");
            } else{
                JOptionPane.showMessageDialog(this, "Empleado no encontrado...", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "El campo Cédula solo admite números", "Aviso", JOptionPane.WARNING_MESSAGE);
            txtCedula.setText("");
        }   
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtCedula.getText());

        if (matcher.matches()){
            int id = Integer.parseInt(txtCedula.getText());
            objEmpleado emp = datos.getEmp( id );

            if (emp != null){
                JOptionPane.showMessageDialog(this, "Empleado encontrado!!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                txtNombre.setText(emp.getNombreEmp());
                txtIdentificacion.setText(emp.getIdEmpleado());
                cmbxEstado.setSelectedIndex(emp.getEstado());
                txtApellido1.setText(emp.getApellido1Emp());
                txtApellido2.setText(emp.getApellido2Emp());
                txtDireccion.setText(emp.getDireccion());
                String tel = String.valueOf(emp.getNumTelefonico());
                txtNumTelef.setText(tel);
            } else {
                JOptionPane.showMessageDialog(this, "El ID digitado no existe", "Aviso", JOptionPane.WARNING_MESSAGE);
                txtCedula.setText("");
            }
            

        } else{
            JOptionPane.showMessageDialog(this, "El campo Cédula solo admite numeros", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

