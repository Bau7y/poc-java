    private void limpiar(){
        txtCedula.setText("");
        txtIdentificacion.setText("");
        txtNombre.setText("");
        txtApellido1.setText("");
        txtApellido2.setText("");
        txtDireccion.setText("");
        txtNumTelef.setText("");
        cmbxEstado.setSelectedIndex(0);
    }
    
    private int comprobar () {
        String regex = "^\\d+$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtCedula.getText());
        if ( matcher.matches()){
            if ( txtCedula.getText().equals("") || txtIdentificacion.getText().equals("") || txtNombre.getText().equals("")){
                return 2;
            }
        } else {
            return 1;
        }
        return 0;
    
    }
    
    private void guardar () {
        int cedula = Integer.parseInt(txtCedula.getText());
        String nombre = txtNombre.getText();
        int estado = cmbxEstado.getSelectedIndex();
        String apellido1 = txtApellido1.getText();
        String empId = txtIdentificacion.getText();
        String apellido2 = txtApellido2.getText();
        int idx = datos.getIdxEmp( cedula );
        String direccion = txtDireccion.getText();
        int tel = Integer.parseInt(txtNumTelef.getText());
        
        objEmpleado emp = new objEmpleado(empId, cedula, tel, estado, nombre, apellido1, apellido2, direccion);
        
        if (idx != -1) {
            datos.modEmp(idx, emp);
            JOptionPane.showMessageDialog(this, "Se ha modificado el empleado correctamente!", "Información", JOptionPane.INFORMATION_MESSAGE);
            this.limpiar();
        
        } else {
            datos.agregarEmp(emp);
            JOptionPane.showMessageDialog(this, "Se ha guardado el empleado correctamente!", "Información", JOptionPane.INFORMATION_MESSAGE);
            this.limpiar();
        }
    
    }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        switch ( comprobar() ) {
            case 0: guardar();
                break;
            case 1: JOptionPane.showMessageDialog(this, "Solo se permiten números para el ID...", "Aviso", JOptionPane.WARNING_MESSAGE);
                break;
            case 2: JOptionPane.showMessageDialog(this, "Debe completar todos los espacios disponibles!", "Aviso", JOptionPane.WARNING_MESSAGE);
                break;
            default:
                throw new AssertionError();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

